# ─── Вспомогательные функции ──────────────────────────────────────────────────

def pick_quality() -> str:
    r = random.random()
    cumulative = 0
    for name, prob, _ in QUALITY_DISTRIBUTION:
        cumulative += prob
        if r < cumulative:
            return name
    return "good"


def generate_smart_scores(quality: str) -> dict:
    ranges = {q: r for q, _, r in QUALITY_DISTRIBUTION}
    low, high = ranges[quality]
    # Генерируем индивидуальные оценки с разбросом
    base = random.uniform(low, high)
    noise = 0.8
    scores = {}
    for criterion in ["S", "M", "A", "R", "T"]:
        raw = base + random.uniform(-noise, noise)
        scores[criterion] = max(1.0, min(5.0, round(raw, 1)))
    scores["index"] = round(sum(scores[c] for c in "SMART") / 5, 2)
    return scores


