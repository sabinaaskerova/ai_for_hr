async def seed_all(session: AsyncSession, force: bool = False):
    # Проверяем, уже заполнено ли
    result = await session.execute(select(Department).limit(1))
    existing = result.scalar_one_or_none()
    if existing and not force:
        log.info("База данных уже заполнена. Используйте --force для перезаполнения.")
        return

    if existing and force:
        log.info("Очищаем существующие данные...")
        for table in ["goal_reviews", "goal_events", "goals", "kpi_timeseries",
                      "documents", "employees", "departments"]:
            await session.execute(text(f"TRUNCATE TABLE {table} RESTART IDENTITY CASCADE"))
        await session.commit()

    log.info("Создаём департаменты...")
    dept_objs = []
    for d in DEPARTMENTS:
        dept = Department(name=d["name"], code=d["code"])
        session.add(dept)
        dept_objs.append(dept)
    await session.flush()

    log.info("Создаём сотрудников...")
    emp_data = make_employees(dept_objs)
    emp_objs = []
    for e in emp_data:
        emp = Employee(
            full_name=e["full_name"],
            position=e["position"],
            grade=e["grade"],
            department_id=e["department_id"],
            email=e["email"],
        )
        session.add(emp)
        emp_objs.append(emp)
    await session.flush()

    # Назначаем менеджеров после flush (у нас есть реальные id)
    id_map = {old["id"]: emp_obj.id for old, emp_obj in zip(emp_data, emp_objs)}
    for old, emp_obj in zip(emp_data, emp_objs):
        if old.get("manager_id"):
            new_mgr_id = id_map.get(old["manager_id"])
            if new_mgr_id and new_mgr_id != emp_obj.id:
                emp_obj.manager_id = new_mgr_id
    await session.flush()

    log.info("Создаём ВНД (160 документов)...")
    doc_data = generate_documents(dept_objs)
    doc_id_map = {}
    for d in doc_data:
        doc = Document(
            title=d["title"],
            doc_type=d["doc_type"],
            department_id=d.get("department_id"),
            content=d["content"],
            keywords=d.get("keywords"),
            effective_date=d.get("effective_date"),
        )
        session.add(doc)
    await session.flush()

    log.info("Создаём KPI timeseries...")
    kpi_data = generate_kpi_timeseries(dept_objs)
    for k in kpi_data:
        kpi = KpiTimeseries(
            department_id=k["department_id"],
            quarter=k["quarter"],
            year=k["year"],
            metric_name=k["metric_name"],
            metric_value=k["metric_value"],
            metric_unit=k["metric_unit"],
            target_value=k["target_value"],
            baseline_value=k["baseline_value"],
        )
        session.add(kpi)
    await session.flush()

    log.info("Создаём цели (9000)...")
    await _seed_goals(session, emp_objs, dept_objs, doc_data)

    await session.commit()
    log.info("Синтетические данные успешно загружены!")


