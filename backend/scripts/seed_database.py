def generate_kpi_timeseries(departments: list[Department]) -> list[dict]:
    """2112 записей KPI по 8 кварталам."""
    records = []
    rec_id = 1
    for dept in departments:
        metrics = KPI_METRICS_BY_DEPT.get(dept.code, [("Производительность", "%", 70, 90)])
        for i, quarter in enumerate(QUARTERS):
            year_q = int(quarter.split("-Q")[0])
            for metric_name, unit, baseline, target in metrics:
                # Плавный тренд от baseline к target
                progress = i / (len(QUARTERS) - 1)
                value = baseline + (target - baseline) * progress * random.uniform(0.8, 1.1)
                value = round(value, 2)

                records.append({
                    "id": rec_id,
                    "department_id": dept.id,
                    "quarter": quarter,
                    "year": year_q,
                    "metric_name": metric_name,
                    "metric_value": value,
                    "metric_unit": unit,
                    "target_value": target,
                    "baseline_value": baseline,
                })
                rec_id += 1

    return records


