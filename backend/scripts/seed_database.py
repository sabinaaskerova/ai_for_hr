def _make_events(goal: Goal) -> list[dict]:
    events = [{"event_type": "created", "comment": "Цель создана сотрудником"}]
    if goal.status in ("submitted", "under_review", "approved", "rejected", "revision_requested"):
        events.append({"event_type": "submitted", "comment": "Цель отправлена на согласование"})
    if goal.status in ("approved",):
        events.append({"event_type": "approved", "comment": "Цель утверждена"})
    if goal.status in ("rejected",):
        events.append({"event_type": "rejected", "comment": "Цель отклонена: не соответствует требованиям SMART"})
    if goal.status in ("revision_requested",):
        events.append({"event_type": "review_requested", "comment": "Запрошена доработка цели"})
    return events


def _status_to_verdict(status: str) -> str:
    mapping = {
        "approved": "approved",
        "rejected": "rejected",
        "revision_requested": "needs_revision",
    }
    return mapping.get(status, "approved")


